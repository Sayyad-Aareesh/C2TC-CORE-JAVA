package mypack;

public class Java_Data_Type {

	public static void main(String[] args) {
		System.out.println("Primitive Data Type");
		System.out.println("Integral Data Type");
		int max_val = 2147483647;
		int min_val = -2147483648;
		short shortMax_val = 32767;
		short shortMin_val = -32768;
		long longMax_val = 9223372036854775807L;
		long longMin_val = -9223372036854775808L;
		byte byteMax_val = 127;
		byte byteMin_val = -128;
		
	
		System.out.println();
		System.out.println("Byte Max_Val :"+byteMax_val);
		System.out.println("Byte  Min_Val :"+byteMin_val);
		
		System.out.println("Short Max_Val :"+shortMax_val);
		System.out.println("Short Min_Val :"+shortMin_val);

		System.out.println("Integer Max_Val :"+max_val);
		System.out.println("Integer Min_Val :"+min_val);
		
		System.out.println("Long Max_Val :"+longMax_val);
		System.out.println("Long Min_Val :"+longMin_val);
		
	}


				
		
	

}
